package LOOPS;

public class Zadanie6 {
    public static void main( String[] args )
    {
        for( int y = 1; y <= 10; y++ )
        {
            for( int x = 1; x <= 10; x++ )
            {
                System.out.print( y * x + "\t" );
            }
            System.out.println();
        }
    }
}
